package com.company;

public class Main {

    public static void main(String[] args) {

        // Assignment Operation

        int x = 3 ;
        int y = x++ ;
        System.out.println(y);
        int z = ++x ;
        System.out.println(z);
        System.out.println(x);
        System.out.println(++z);
        System.out.println(z++);



    }
}
